public class Celcius2Fahrenheit {
    
    public static void main(String[] args) {
        double iCelcius = 20, iFahrenheit;
        
        iFahrenheit = (9.0 / 5.0) * iCelcius + 32;
        
        System.out.println("The fahrenheit equivalent of " + iCelcius + " celcius is " + iFahrenheit);
    }
    
}
